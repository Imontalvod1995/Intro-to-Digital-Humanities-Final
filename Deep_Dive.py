import pytesseract

from PIL import Image

import stanza

moro_count = []


moro_cord  =  pytesseract.image_to_string(Image.open('00000009.jpg'), lang='spa')

stanza.download('es')

nlpz = stanza.Pipeline('es')

moro_nlp = nlpz(moro_cord)

moro_entz = list(moro_nlp.entities)

for jaja in moro_entz:
    moro_count.append(moro_entz.count(jaja))

moro_dict = dict(zip(moro_entz, moro_count))

print(moro_dict)










































































































































































































































































































































































































